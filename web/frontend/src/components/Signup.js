import React, { useState } from 'react';
import axios from 'axios';
import { useNavigate } from 'react-router-dom';
import 'bootstrap/dist/css/bootstrap.min.css';
import './Signup.css'; // Import custom CSS
import config from '../Config.json'

function Signup() {
    const [username, setUsername] = useState('');
    const [password, setPassword] = useState('');
    const [email, setEmail] = useState('');
    const navigate = useNavigate();

    const handleSubmit = async (e) => {
        e.preventDefault();
        try {
            const res = await axios.post(config.python_url+'/signup', {
                username,
                password,
                email
            });
            if (localStorage.getItem('token')) {
                localStorage.removeItem('token');
            }
            if (res.data && res.data.access_token) {
                localStorage.setItem('token', res.data.access_token); // Store the token in localStorage
                console.log('Signup successful and token stored');
                navigate('/home');
            } else {
                console.error('No token received');
                alert('Signup failed, no token received');
            }
        } catch (error) {
            console.error('Signup failed:', error);
            alert('Singup failed, username or email already exists');
        }
    };

    return (
        <div className="signup-container">
            <div className="signup-card">
                <h2 className="signup-title">Sign Up</h2>
                <form onSubmit={handleSubmit} className="signup-form">
                    <div className="form-group">
                        <label htmlFor="username" className="form-label">
                            Username
                        </label>
                        <input
                            type="text"
                            className="form-control"
                            id="username"
                            value={username}
                            onChange={(e) => setUsername(e.target.value)}
                            placeholder="Enter username"
                            required
                        />
                    </div>
                    <div className="form-group">
                        <label htmlFor="password" className="form-label">
                            Password
                        </label>
                        <input
                            type="password"
                            className="form-control"
                            id="password"
                            value={password}
                            onChange={(e) => setPassword(e.target.value)}
                            placeholder="Enter password"
                            required
                        />
                    </div>
                    <div className="form-group">
                        <label htmlFor="email" className="form-label">
                            Email
                        </label>
                        <input
                            type="email"
                            className="form-control"
                            id="email"
                            value={email}
                            onChange={(e) => setEmail(e.target.value)}
                            placeholder="Enter email"
                            required
                        />
                    </div>
                    <button type="submit" className="btn btn-primary signup-btn">
                        Sign Up
                    </button>
                </form>
            </div>
        </div>
    );
}

export default Signup;
